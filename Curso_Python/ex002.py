#Python Casting(funções de construtor)
#Pode haver momentos em que você deseja especificar um tipo para uma variável

#Especificando inteiros
x = int(1)   # vale 1
y = int(2.8) # vale 2
z = int("3") # vale 3

#Especificando Floats
a = float(1)     # vale 1.0
b = float(2.8)   # vale 2.8
c = float("3")   # vale 3.0
d = float("4.2") # vale 4.2

#Especificando Strings
e = str("s1") # vale 's1'
f = str(2)    # vale '2'
g = str(3.0)  # vale '3.0'

