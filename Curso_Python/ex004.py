#Metodos de String

#strip() remove todo espa√ßo em branco no inicio e no final
msg = " Filipe "
print(msg.strip())

#lower() retorna a string em letras minusculas
msg1 = "FILIPE"
print(msg1.lower())

#upper() retorna a string em letras maiusculas
msg2 = "filipe"
